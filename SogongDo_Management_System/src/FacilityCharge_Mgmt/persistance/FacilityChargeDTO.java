package FacilityCharge_Mgmt.persistance;

public class FacilityChargeDTO {
	private int chargeID;
	private int touristSiteID;
	private int facilityID;
	private String touristSiteName;
	private String facilityName;
	private int charge;
	
	public FacilityChargeDTO(int chargeID, String touristSiteName, String facilityName, int charge) {
		super();
		this.chargeID = chargeID;
		this.touristSiteName = touristSiteName;
		this.facilityName = facilityName;
		this.charge = charge;
	}

	public FacilityChargeDTO(int touristSiteID, int facilityID, int charge) {
		super();
		this.touristSiteID = touristSiteID;
		this.facilityID = facilityID;
		this.charge = charge;
	}

	public int getChargeID() {
		return chargeID;
	}

	public void setChargeID(int chargeID) {
		this.chargeID = chargeID;
	}

	public int getTouristSiteID() {
		return touristSiteID;
	}

	public void setTouristSiteID(int touristSiteID) {
		this.touristSiteID = touristSiteID;
	}

	public int getFacilityID() {
		return facilityID;
	}

	public void setFacilityID(int facilityID) {
		this.facilityID = facilityID;
	}

	public String getTouristSiteName() {
		return touristSiteName;
	}

	public void setTouristSiteName(String touristSiteName) {
		this.touristSiteName = touristSiteName;
	}

	public String getFacilityName() {
		return facilityName;
	}

	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}

	public int getCharge() {
		return charge;
	}

	public void setCharge(int charge) {
		this.charge = charge;
	}
}
